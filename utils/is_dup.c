#include "../include/checker.h"

int	is_dup(char **av, int i)
{
	int j;
	
	if (!av[i + 1])
		return (0);
	j = i;
	while (av[i + 1])
	{
		if (!ft_strcmp(av[j], av[i + 1]))
			return (1);
		i++;
	}
	return (0);
}
